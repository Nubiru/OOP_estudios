// Ejercicio 18: producto de 87 y 94 usando "numero1" y "numero2"
function producto18(numero1: number, numero2: number): number {
    return numero1 * numero2;
}

let numero1: number = 87;
let numero2: number = 94;
console.log(`El producto de ${numero1} y ${numero2} es: ${producto18(numero1, numero2)}`);
