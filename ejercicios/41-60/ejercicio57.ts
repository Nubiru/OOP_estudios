// Ejercicio 57: numero del 1 al 5 a texto, usando switch
let numero57: number = 3;  // Simula entrada del usuario
switch (numero57) {
    case 1: console.log("uno"); break;
    case 2: console.log("dos"); break;
    case 3: console.log("tres"); break;
    case 4: console.log("cuatro"); break;
    case 5: console.log("cinco"); break;
    default: console.log("fuera de rango"); break;
}
