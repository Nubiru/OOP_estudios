// Ejercicio 50: decir si dos numeros son iguales, y si no, cual es el mayor
let a50: number = 7;   // Simula entrada del usuario
let b50: number = 11;  // Simula entrada del usuario
if (a50 == b50) {
    console.log(`${a50} y ${b50} son iguales`);
} else if (a50 > b50) {
    console.log(`El mayor es ${a50}`);
} else {
    console.log(`El mayor es ${b50}`);
}
